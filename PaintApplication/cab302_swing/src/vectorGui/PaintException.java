package vectorGui;

public class PaintException extends Exception {

    public PaintException() {
        super();
    }

    public PaintException(String message) {
        super(message);
    }

    public PaintException(String message, Throwable cause) {
        super(message, cause);
    }

    public PaintException(Throwable cause) {
        super(cause);
    }
}
